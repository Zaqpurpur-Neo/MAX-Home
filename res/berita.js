function formatTanggal(dateString) {
	const date = new Date(dateString)
	return date.toLocaleDateString('id-ID', {
    	day: 'numeric',
    	month: 'long',
    	year: 'numeric'
	})
}

function createBerita(container, tanggal, sumber, judul, url, gambar) {
	const berita = document.createElement('berita')
	berita.classList.add("berita")
	berita.innerHTML = `
		<img ${gambar === "" ? "" : ("src=" + gambar)}>

		<div class="content">
		<div class="header-information">
			<p>${tanggal === null ? "" : formatTanggal(tanggal)}</p>
			<p>${sumber}</p>
        </div>
		<h3>${judul}</h3>
		<a href="${url}">Selengkapnya</a>
		</div>
	`

	container.append(berita)
}

document.addEventListener("DOMContentLoaded", () => {
	const container = document.querySelector(".berita-container");

	beritaSemua.forEach(item => {
		createBerita(container, item.tanggal, item.sumber, item.judul, item.url, item.gambar)
	})


	const intersectionItem = new IntersectionObserver(entries => {
		entries.forEach(entry => {
			if(entry.isIntersecting) {
				entry.target.style.transform = 'translateY(0)'
				entry.target.style.opacity = '1'
			} else {
				entry.target.style.opacity = '0'
			}
		});
	})

	Array.from(container.children).forEach(item => {
		item.style.transition = "all .6s ease"
		item.style.transform = 'translateY(4em)'
		item.style.opacity = '0'

		intersectionItem.observe(item)
	})

})
